package com.metz.numeric.school.user;

public class User {

	private String name;
	private String passWord;

	public String getName() {
		return name;
	}

	public String getPassWord() {
		return passWord;
	}

	public User(String name, String passWord) {
		super();
		this.name = name;
		this.passWord = passWord;
	}

}
